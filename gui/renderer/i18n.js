if (!I18N[_lang]) _lang = (I18N[(navigator.language || "de").slice(0, 2)] ? (navigator.language || "de").slice(0, 2) : "de");

function getLang() { return _lang; }

function t(key, params) {
  let s = (I18N[_lang] && I18N[_lang][key] != null) ? I18N[_lang][key] : (I18N.de[key] != null ? I18N.de[key] : key);
  if (params) for (const k in params) s = s.split("{" + k + "}").join(params[k]);
  return s;
}

// Wendet alle data-i18n* Attribute an (Text, HTML, placeholder, title).
function applyStaticI18n() {
  document.querySelectorAll("[data-i18n]").forEach((el) => { el.textContent = t(el.getAttribute("data-i18n")); });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => { el.innerHTML = t(el.getAttribute("data-i18n-html")); });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => { el.placeholder = t(el.getAttribute("data-i18n-ph")); });
  document.querySelectorAll("[data-i18n-title]").forEach((el) => { el.title = t(el.getAttribute("data-i18n-title")); });
  document.documentElement.lang = _lang;
}

function setLang(l) {
  if (!I18N[l]) return;
  _lang = l;
  localStorage.setItem("lang", l);
  applyStaticI18n();
  if (window.api && window.api.setLang) window.api.setLang(l);   // Main-Prozess (Notifications) informieren
  if (typeof window.onLangChange === "function") window.onLangChange();
}
